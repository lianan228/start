package com.longxi.www.structure;

//增强型for循环

public class For_Demo04 {
    public static void main(String[] args) {
        int[] numbers = {10,20,30,40,50};

        //遍历数组的元素
        for(int x:numbers){
            System.out.println(x);
            //将numbers数组内的元素赋值给x
            //遍历输出
        }//10 20 30 40 50
    }
}
