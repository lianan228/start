package com.longxi.www.IO.More;

public class Student_Demo04 {
    private int num;
    private String name;
    private int age;
    private String place;

    public Student_Demo04(){}
    public Student_Demo04(int num, String name, int age, String place) {
        this.num = num;
        this.name = name;
        this.age = age;
        this.place = place;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

}
