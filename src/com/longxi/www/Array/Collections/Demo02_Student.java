package com.longxi.www.Array.Collections;

public class Demo02_Student {
    private String name;
    private int age;

    public Demo02_Student(){}
    public Demo02_Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
