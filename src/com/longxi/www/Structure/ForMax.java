package com.longxi.www.Structure;

public class ForMax {

    public static void main(String[] args) {
        int[] arr = {1,2,3,4,5};
        //类型 变量名 ：数组名
        for (int i: arr) {
            System.out.println(i);
        }
        String[] str = {"hello","world"};
        for(String s : str){
            System.out.println(s);
        }
    }
}
